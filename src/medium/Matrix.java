	public int[][] updateMatrix(int[][] matrix) {
		if (matrix == null || matrix.length == 0) {
			return new int[][] {};
		}
		final int response[][] = new int[matrix.length][matrix[0].length];
		final boolean visited[][] = new boolean[matrix.length][matrix[0].length];
		for (int i = 0; i < matrix.length; i++) {
			for (int j = 0; j < matrix[0].length; j++) {
				response[i][j] = Integer.MAX_VALUE;
			}
		}
		recursivelySolve(0, 0, matrix, response, visited);
		return response;
	}

	private int[][] nonrecursiveDfs(int[][] matrix) {
		final int response[][] = new int[matrix.length][matrix[0].length];
		final boolean visited[][] = new boolean[matrix.length][matrix[0].length];
		for (int i = 0; i < matrix.length; i++) {
			for (int j = 0; j < matrix[0].length; j++) {
				response[i][j] = Integer.MAX_VALUE;
			}
		}
		Stack<Pair<Integer, Integer>> stack = new Stack<Pair<Integer, Integer>>();
		stack.push(new Pair<Integer, Integer>(0, 0));
		while (!stack.isEmpty()) {
			Pair<Integer, Integer> p = stack.peek();
			if (matrix[p.x][p.y] == 0) {
				response[p.x][p.y] = 0;
				stack.pop();
			}
			visited[p.x][p.y] = true;
			Set<Direction> validNeighbours = new HashSet<Direction>();
			for (Direction direction : Direction.values()) {
				validNeighbours.add(direction);
			}
			if (p.x == 0) {
				validNeighbours.remove(Direction.UP);
			}
			if (p.y == 0) {
				validNeighbours.remove(Direction.LEFT);
			}
			if (p.x == matrix.length - 1) {
				validNeighbours.remove(Direction.DOWN);
			}
			if (p.y == matrix[0].length - 1) {
				validNeighbours.remove(Direction.RIGHT);
			}
			for (Direction direction : validNeighbours) {
				if (direction == Direction.UP) {
					if (matrix[p.x - 1][p.y] == 0 && matrix[p.x][p.y] != 0) {
						response[p.x][p.y] = 1;
					}
					if (!visited[p.x - 1][p.y]) {
						stack.push(new Pair<Integer, Integer>(p.x - 1, p.y));
					}
				}
				if (direction == Direction.DOWN) {
					if (matrix[p.x + 1][p.y] == 0 && matrix[p.x][p.y] != 0) {
						response[p.x][p.y] = 1;
					}
					if (!visited[p.x + 1][p.y]) {
						stack.push(new Pair<Integer, Integer>(p.x + 1, p.y));
					}
				}
				if (direction == Direction.LEFT) {
					if (matrix[p.x][p.y - 1] == 0 && matrix[p.x][p.y] != 0) {
						response[p.x][p.y] = 1;
					}
					if (!visited[p.x][p.y - 1]) {
						stack.push(new Pair<Integer, Integer>(p.x, p.y - 1));
					}
				}
				if (direction == Direction.RIGHT) {
					if (matrix[p.x][p.y + 1] == 0 && matrix[p.x][p.y] != 0) {
						response[p.x][p.y] = 1;
					}
					if (!visited[p.x][p.y + 1]) {
						stack.push(new Pair<Integer, Integer>(p.x, p.y + 1));
					}
				}
			}
		}
		return response;
	}

	private void recursivelySolve(int rowIndex, int colIndex, int[][] matrix, int[][] response, boolean[][] visited) {
		System.out.print(rowIndex+" ");
		System.out.print(colIndex+"\n");
		if (matrix[rowIndex][colIndex] == 0) {
			response[rowIndex][colIndex] = 0;
		}
		int minimum = Integer.MAX_VALUE;
		Set<Direction> validNeighbours = new HashSet<Direction>();
		for (Direction direction : Direction.values()) {
			validNeighbours.add(direction);
		}
		if (rowIndex == 0) {
			validNeighbours.remove(Direction.UP);
		}
		if (colIndex == 0) {
			validNeighbours.remove(Direction.LEFT);
		}
		if (rowIndex == matrix.length - 1) {
			validNeighbours.remove(Direction.DOWN);
		}
		if (colIndex == matrix[0].length - 1) {
			validNeighbours.remove(Direction.RIGHT);
		}

		for (Direction direction : validNeighbours) {
			if (direction == Direction.UP) {
				if (matrix[rowIndex - 1][colIndex] == 0 && matrix[rowIndex][colIndex] != 0) {
					response[rowIndex][colIndex] = 1;
				}
				if (!visited[rowIndex - 1][colIndex]) {
					visited[rowIndex-1][colIndex] = true;
					recursivelySolve(rowIndex - 1, colIndex, matrix, response, visited);
				}
			}
			if (direction == Direction.DOWN) {
				
				if (matrix[rowIndex + 1][colIndex] == 0 && matrix[rowIndex][colIndex] != 0) {
					response[rowIndex][colIndex] = 1;
				}
				if (!visited[rowIndex + 1][colIndex]) {
					visited[rowIndex+1][colIndex] = true;
					recursivelySolve(rowIndex + 1, colIndex, matrix, response, visited);
				}
			}
			if (direction == Direction.LEFT) {
				
				if (matrix[rowIndex][colIndex - 1] == 0 && matrix[rowIndex][colIndex] != 0) {
					response[rowIndex][colIndex] = 1;
				}
				if (!visited[rowIndex][colIndex - 1]) {
					visited[rowIndex][colIndex - 1] = true;
					recursivelySolve(rowIndex, colIndex - 1, matrix, response, visited);
				}
			}
			if (direction == Direction.RIGHT) {
				
				if (matrix[rowIndex][colIndex + 1] == 0 && matrix[rowIndex][colIndex] != 0) {
					response[rowIndex][colIndex] = 1;
				}
				if (!visited[rowIndex][colIndex + 1]) {
					visited[rowIndex][colIndex + 1] = true;
					recursivelySolve(rowIndex, colIndex + 1, matrix, response, visited);
				}
			}
		}

		if (response[rowIndex][colIndex] == Integer.MAX_VALUE) {
			for (Direction direction : validNeighbours) {
				if (direction == Direction.UP) {
					minimum = Math.min(minimum, response[rowIndex - 1][colIndex]);
				}
				if (direction == Direction.DOWN) {
					minimum = Math.min(minimum, response[rowIndex + 1][colIndex]);
				}
				if (direction == Direction.LEFT) {
					minimum = Math.min(minimum, response[rowIndex][colIndex - 1]);
				}
				if (direction == Direction.RIGHT) {
					minimum = Math.min(minimum, response[rowIndex][colIndex + 1]);
				}
			}
			response[rowIndex][colIndex] = minimum + 1;
		}
	}

